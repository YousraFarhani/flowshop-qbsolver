// Taillard benchmarks dataset
// Function to generate a job matrix based on the instance name
export function generateJobMatrix(instance: string): JobMatrix {
  // Check if the instance is in the taillardDatasets
  if (instance in taillardDatasets) {
    const processingTimes = taillardDatasets[instance as keyof typeof taillardDatasets];
    return {
      jobs: processingTimes.length,
      machines: processingTimes[0].length,
      processingTimes
    };
  }
  
  // If it's not a predefined dataset, parse the instance string
  // Format is expected to be like "tai20x5" where 20 is jobs and 5 is machines
  if (instance !== "custom") {
    const match = instance.match(/tai(\d+)x(\d+)/);
    if (match) {
      const jobs = parseInt(match[1]);
      const machines = parseInt(match[2]);
      
      // Generate random processing times
      const processingTimes = Array.from({ length: jobs }, () => 
        Array.from({ length: machines }, () => Math.floor(Math.random() * 50) + 10) // Random times between 10 and 60
      );
      
      return {
        jobs,
        machines,
        processingTimes
      };
    }
  }
  
  // Default case or "custom" instance
  // Return a placeholder matrix - the actual matrix will be set by the Dashboard component
  return {
    jobs: 5,
    machines: 3,
    processingTimes: Array.from({ length: 5 }, () => Array.from({ length: 3 }, () => 0))
  };
}

// Function to generate a solution result
export function generateSolutionResult(
  solverName: string,
  jobMatrix: JobMatrix,
  timeLimit: number,
  isSmart: boolean = false
): SolutionResult {
  const { jobs, machines, processingTimes } = jobMatrix;
  
  // Generate a sample schedule
  const schedule: Operation[] = [];
  let currentTime = 0;
  
  // Generate a random job sequence
  const jobSequence = Array.from({ length: jobs }, (_, i) => i + 1)
    .sort(() => Math.random() - 0.5);
  
  // Calculate a makespan (this is a simplified version)
  const makespan = Math.floor(Math.random() * 500) + 1000; // Random makespan between 1000 and 1500
  
  // Simulate execution time
  const executionTime = Math.random() * 5 + 1; // Random execution time between 1 and 6 seconds
  
  return {
    solver: solverName,
    makespan,
    schedule,
    logs: [
      `[INFO] Starting ${solverName} for instance with ${jobs} jobs and ${machines} machines`,
      `[INFO] Time limit: ${timeLimit}s`,
      `[INFO] Execution time: ${executionTime.toFixed(2)}s`,
      `[INFO] Makespan: ${makespan}`
    ],
    numJobs: jobs,
    executionTime,
    jobSequence,
    quboType: isSmart ? "Smart" : "Basic"
  };
}

